void main(){

  // Arithmetic Operators

  num a = 10;
  num b = 20;

  // Addition
  print(a + b); // 30

  // Subtraction
  print(a - b); // -10

  // Multiplication
  print(a * b); // 200

  // Division
  print(a / b); // 0.5

  // Remainder
  print(a % b); // 10

  // Integar Division
  print(a ~/ b); // 0

}