void main() {
  // Equality & Relational Operator

  int num1 = 10;
  int num2 = 20;

  // Equal to
  print(num1 == num2);

  // Not equal to
  print(num1 != num2);

  // Greater than
  print(num1 > num2);

  // Less than
  print(num1 < num2);

  // Greater than or equal to
  print(num1 >= num2);

  // Less than or equal to
  print(num1 <= num2);
}
